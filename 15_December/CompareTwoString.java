package string;

public class CompareTwoString 
{
public static void main(String[] args) 
{   
	 
	String S1="JAYSHREE";
	String S2="CHAVAN";
	//Compare the two String Value
	System.out.println("Compare the two String Value");
	System.out.println(S1==S2);
	//Compare the Two String Using String Function
	System.out.println("Compare the Two String Using String Function");
	System.out.println(S2.compareTo(S1));
 
}
}
